              public void CodigoUsuarios(){
          String SQL = "";

                 SQL="SELECT MAX(USU_id) FROM usuario ";   
          String c="";
           try {
            Statement st = cn.createStatement();
            ResultSet rs=st.executeQuery(SQL);
            while(rs.next())
            {              
                 c=rs.getString(1); 
            }
//               System.out.println(""+c);   
                  
            if(c==null){
                txtcod.setText("1");
            }
            else{
                
int codigo=Integer.parseInt(c);
codigo++;       
txtcod.setText(""+codigo); System.out.println(""+codigo);
            }
             } catch (SQLException ex) {
           Logger.getLogger(Ingreso_Usuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
           
              
         } 
    
